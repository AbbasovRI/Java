package homework.homework02;

public class FourthTask {
    public static void main(String[] args) {
        char[][] Setka = new char[10][10];

        for(int i = 0; i < 10; ++i) {
            for(int j = 0; j < 10; ++j) {
                System.out.printf("- ", Setka[i][j]);
            }

            System.out.println();
        }

    }
}
